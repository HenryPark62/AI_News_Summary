# summarizer/prompt_utils.py

def create_prompt(text, style):
    if style == "brief":
        return (
            "다음은 뉴스 기사 전문입니다.\n\n"
            f"{text}\n\n"
            "이 뉴스를 한국어로 간결하고 핵심만 요약해 주세요. (2~3줄 이내) 그리고 출처 번호나 [1], [2] 등의 표시는 포함하지 말고 작성해 주세요. 오직 한국어로만 작성해 주세요."
        )
    elif style == "detailed":
        return (
            "다음은 뉴스 기사 전문입니다.\n\n"
            f"{text}\n\n"
            "이 뉴스를 한국어로 상세하고 풍부하게 요약해 주세요. (5~7줄 정도) 그리고 출처 번호나 [1], [2] 등의 표시는 포함하지 말고 작성해 주세요. 오직 한국어로만 작성해 주세요."
        )
    else:
        return (
            "다음은 뉴스 기사 전문입니다.\n\n"
            f"{text}\n\n"
            "이 뉴스를 한국어로 간결하고 명확하게 3~5줄 이내로 요약해 주세요. 그리고 출처 번호나 [1], [2] 등의 표시는 포함하지 말고 작성해 주세요. 오직 한국어로만 작성해 주세요."
        )